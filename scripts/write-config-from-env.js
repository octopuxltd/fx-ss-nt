#!/usr/bin/env node
/**
 * Writes repo-root config.js from environment variables (Netlify, CI, local).
 * Keys are still exposed to anyone who loads the site — same as a checked-in
 * config.js in a private deploy; use restricted API keys where possible.
 */
'use strict';

const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const outFile = path.join(root, 'config.js');

const trim = (v) => (v == null ? '' : String(v).trim());
const keys = {
    OPENROUTER_API_KEY: trim(process.env.OPENROUTER_API_KEY),
    OPENAI_API_KEY: trim(process.env.OPENAI_API_KEY),
    PEXELS_API_KEY: trim(process.env.PEXELS_API_KEY),
    CLAUDE_API_KEY: trim(process.env.CLAUDE_API_KEY),
};

const body =
    '// Generated by scripts/write-config-from-env.js — do not commit.\n' +
    `window.API_CONFIG = ${JSON.stringify(keys, null, 4)};\n`;

fs.writeFileSync(outFile, body, 'utf8');

const summary = Object.entries(keys)
    .map(([k, v]) => `${k}=${v ? 'set' : 'empty'}`)
    .join(', ');
console.log(`[write-config-from-env] Wrote ${path.relative(root, outFile)} (${summary})`);
